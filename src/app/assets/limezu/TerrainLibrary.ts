let cachedPaintMap: TerrainPaintMap = {};
export function terrainCoordKey(x:number,y:number){return `${x},${y}`;}
export function getTerrainAsset(id:string|undefined):LimeZuRuntimeAsset{return TERRAIN_ASSETS.find(a=>a.id===id||a.sourceAssetId===id)??TERRAIN_ASSETS[0];}
export function readTerrainPaintMap():TerrainPaintMap{if(typeof window==="undefined")return cachedPaintMap;try{cachedPaintMap=JSON.parse(window.localStorage.getItem(PAINT_STORAGE_KEY)??"{}");}catch{cachedPaintMap={};}return cachedPaintMap;}
export function writeTerrainPaintMap(next:TerrainPaintMap){cachedPaintMap={...next};if(typeof window!=="undefined"){window.localStorage.setItem(PAINT_STORAGE_KEY,JSON.stringify(cachedPaintMap));window.dispatchEvent(new CustomEvent("limezu:terrain-paint-changed"));window.dispatchEvent(new CustomEvent("satiria:limezu-terrain-paint-changed"));}}
export function paintTerrainAssetAt(x:number,y:number,assetId:string){writeTerrainPaintMap({...readTerrainPaintMap(),[terrainCoordKey(x,y)]:assetId});}
export function eraseTerrainAssetAt(x:number,y:number){const next={...readTerrainPaintMap()};delete next[terrainCoordKey(x,y)];writeTerrainPaintMap(next);}
export function terrainAssetForCoord(x:number,y:number):LimeZuRuntimeAsset|undefined{const assetId=readTerrainPaintMap()[terrainCoordKey(x,y)];return assetId?getTerrainAsset(assetId):undefined;}
export function terrainImageForCoord(x:number,y:number):string|undefined{return terrainAssetForCoord(x,y)?.src;}
export function terrainImageForKind(kind:"grass"|"path"|"road"|"water"|"sand"|"plaza"|"stone"|"wood"):string{const preferred=TERRAIN_ASSETS.find(asset=>{if(kind==="grass")return assetMatches(asset,["grass"]);if(kind==="path")return assetMatches(asset,["dirt","path"]);if(kind==="road")return assetMatches(asset,["asphalt","road"]);if(kind==="water")return assetMatches(asset,["water"]);if(kind==="sand")return assetMatches(asset,["sand","beach"]);if(kind==="plaza")return assetMatches(asset,["sidewalk","pavement","stone"]);if(kind==="stone")return assetMatches(asset,["stone","sidewalk"]);if(kind==="wood")return assetMatches(asset,["wood","floor"]);return false;});return (preferred??TERRAIN_ASSETS[0])?.src??"";}
